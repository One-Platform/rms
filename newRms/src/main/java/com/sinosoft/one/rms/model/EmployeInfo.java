package com.sinosoft.one.rms.model;

public class EmployeInfo {
	
	private String userCode;
	
	private String userName;
	
	private String comCName;
	
	private String openQXAndSJ;
	
	private String openWindow;

	public String getUserCode() {
		return userCode;
	}

	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getComCName() {
		return comCName;
	}

	public void setComCName(String comCName) {
		this.comCName = comCName;
	}

	public String getOpenQXAndSJ() {
		return openQXAndSJ;
	}

	public void setOpenQXAndSJ(String openQXAndSJ) {
		this.openQXAndSJ = openQXAndSJ;
	}

	public String getOpenWindow() {
		return openWindow;
	}

	public void setOpenWindow(String openWindow) {
		this.openWindow = openWindow;
	}
	
	

}
